package com.callor.values;

public class Values_05 {

	public static void main(String[] args) {
		
		// 코드에서 동등연산자(==)
		// 어떤 값이 같은지?
		// 값이 같으면 결과는 true
		// 값이 다르면 결과는 false
	 	System.out.println(3 == 3);
		System.out.println(4 == 3);
		System.out.println(5 == 2);
		
		System.out.println(3 > 3);
		System.out.println(3 < 3);
		
		System.out.println(4 > 3);
		System.out.println(4 < 3);
		
		System.out.println(3 >= 3);
		System.out.println(3 <= 3);
		
		System.out.println(4 >= 3);
		System.out.println(4 <= 3);
		
		// != : 값이 서로 같이 아니한가?
		//		값이 서로 다르냐
		System.out.println(3 != 3);
		// 다른언어에서 같은뜻 System.out.println(3 <> 3);
	}	
}